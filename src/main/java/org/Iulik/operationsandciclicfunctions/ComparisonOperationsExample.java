package org.Iulik.operationsandciclicfunctions;

public class ComparisonOperationsExample {
    public static void main(String[] args) {
        int w = 28;
        int k = 14;

        //Equal with
        boolean isEqual = w == k;
        System.out.println("Equal with: " + isEqual);

        //Different from
        boolean isNotEqual = w != k;
        System.out.println("Different from: " + isNotEqual);

        //Is Greater then
        boolean isGreaterThan = w > k;
        System.out.println("Is Greater then: " + isGreaterThan);

        //Is Less than
        boolean isLessThan = w < k;
        System.out.println("Is Less Than: " + isLessThan);

        //Is Greater then or Equal
        boolean isGreaterThanOrEqual = w >= k;
        System.out.println("Is Greater then or Equal: " + isGreaterThanOrEqual);

        //Is Less than or Equal
        boolean isLessThanOrEqual = w == k;
        System.out.println("Is Less than or Equal: " + isLessThanOrEqual);


    }
}
